import os
import csv
import re

def extract_text(file_path, keyword_groups):
    # 파일 읽기
    with open(file_path, 'r', encoding='utf-8') as file:
        text = file.read()
    
    extracted_sections = {}

    for group in keyword_groups:
        start_keywords = group['start_keywords']
        end_keywords = group['end_keywords']
        
        # 키워드들을 정규식 패턴으로 변환
        start_pattern = '|'.join([rf"(?:^|\n){re.escape(keyword)}(?:$|\n)" for keyword in start_keywords])
        end_pattern = '|'.join([rf"(?:^|\n){re.escape(keyword)}(?:$|\n)" for keyword in end_keywords])
        
        # 정규식으로 범위 탐색
        pattern = rf"({start_pattern})(.*?)(?=({end_pattern})|$)"
        matches = re.findall(pattern, text, re.DOTALL)
        
        # 섹션 정보 저장
        for match in matches:
            start_keyword, content, end_keyword = match
            extracted_sections[group['section_name']] = content.strip()

    return extracted_sections

def process_directory(directory_path, keyword_groups, output_csv):
    # 디렉토리 내 모든 텍스트 파일을 탐색
    files = [f for f in os.listdir(directory_path) if f.endswith('.txt')]

    # CSV 파일 작성 준비
    with open(output_csv, 'w', newline='', encoding='utf-8') as csvfile:
        fieldnames = ['파일명', '주요 업무', '필수 자격', '우대 사항']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        
        writer.writeheader()

        # 각 파일에 대해 텍스트 추출 후 CSV에 저장
        for file_name in files:
            file_path = os.path.join(directory_path, file_name)
            extracted_data = extract_text(file_path, keyword_groups)

            # 각 섹션의 내용을 파일에 맞게 작성
            row = {
                '파일명': file_name,
                '주요 업무': extracted_data.get('주요업무', ''),
                '필수 자격': extracted_data.get('자격요건', ''),
                '우대 사항': extracted_data.get('우대사항', ''),
            }

            writer.writerow(row)
            print(f"{file_name}에서 데이터를 추출하여 {output_csv}에 저장했습니다.")

